const students = [];

for (let i = 0; i < 100; i++) {
    const name = `Студент ${i + 1}`;
    const faculty = faculties[Math.floor(Math.random() * faculties.length)];
    const course = Math.floor(Math.random() * 4) + 1;
    const grades = [];

    for (let j = 0; j < 12; j++) {
        grades.push(Math.floor(Math.random() * 3) + 3);
    }

    students.push({
        name,
        faculty,
        course,
        grades
    });
}

